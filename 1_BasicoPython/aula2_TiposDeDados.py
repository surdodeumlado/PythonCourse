"""
Python é de typagem dinamica e forte
existem 4 tipos aq
bool = True or False
int = numeros inteiros
float = numeros com ponto flutuante
str = strings, texto
"""

print(type(1234))
print(type('Daniel'))
print(type(12.34))
print(type(True))

# Caractere de escape é a \, basicamente o programa não lê a barra invertida dentro de aspas

print('Olá \"mundo\"!')

# Se quisermos que o caractere de escape apareça, colocamos o prefixo r antes das aspas

print(r'Olá \"mundo\"!')
